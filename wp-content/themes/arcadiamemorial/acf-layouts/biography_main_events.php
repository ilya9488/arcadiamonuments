            <h2>Biography</h2>
            <p>Joan was born in Tampa, Florida, in 1930 to Joshua and Ellis Walcott. Her father was a truck driver and her mother was a housewife.</p>
            <p>  Joan was the youngest child in the family and had two older sisters, Abby and Eva, who loved her from an early age and were very protective and helpful to her parents.
            Joan was the first in her family to graduate from university and get a higher education. Her parents and sisters were very proud of Joan, and the training and knowledge she received at the University of South Florida helped her start her own business.</p>
            <p>Not only did the university give Joan the knowledge, but it was also where she met John, her future husband. Much later, at family events, she repeatedly told me what attracted her to John - his sharp mind, sense of humour, kindness and the fact that he was a handsome young man. How could she not fall in love with him!!!</p>
            <p>John and Joan got married right after graduation. They didn’t want to have a big wedding, so they arranged a small wedding at John’s parents’ house near Tampa. The money they had saved on organising a relatively small wedding, they spent on a new car and a house full of furniture.</p>
            <p>Both Joan and her husband John loved road trips. When they were young, they could spend days driving to scenic, but not very famous places.</p>
            <p>They had always been attracted to hiking, travelling, and the outdoor activities. And this hobby later became their business. They moved to Alpharetta, Georgia, where they opened their own hiking equipment shop.</p>
            <p>Joan has always loved pets, and although she never had a pet of her own before they moved to Georgia, she often volunteered at various animal shelters throughout Tampa. The older neighbours in her home area probably still remember the young girl with the sparkle in her eyes telling them about the beautiful and “smartest dog in the world” who was looking for a new home.</p>
            <p>Soon, after moving to Alpharetta, Joan and John became a joyful mother and father of two babies, Daniel and Joel. Joan loved spending time with her sons. She often read fiction and fantasy books for them. They inherited her passion and love of travelling. Daniel became a game designer and Joel joined the Navy.</p>
            <p>John and Joan were always proud of their sons and took every opportunity to spend time with them. Joan always loved people and made new friends easily. So it was no surprise that she became friends with Jane and Diana, the wives of her two sons.</p>
            <p>Soon the whole extended family was travelling together. However, together with her husband, they always travelled in their own car and stayed in different hotels away from their children. She said that she definitely enjoyed spending time with her sons and their wives, but she did not want to inconvenience them and wanted to give them as much freedom as possible.</p>
            <p>As time went on, Joan could no longer travel as much as she used to when she was young. So she concentrated on raising her grandchildren.</p>
            <p>Joan never stayed in one place and was constantly looking for new challenges. Together with her three grandchildren, she mastered video games, and they enjoyed playing Crash Bandicoot together. But she was especially good at racing and rallying. No one in the family ever beat her record!</p>
            <p>Whatever games Joan played with her sons or grandchildren, she never succumbed, and always played her best. As Sam, Bob and Christy said, “Our grandmother is fierce in games, but a kind bunny in life”. She never raised her voice to any of her children or grandchildren. Joan always explained to them why they couldn’t or should do something.</p>
            <p>Joan was a tremendous authority in her family and her family will always miss her loving tone of voice, wise advice, willingness to tackle new challenges and vivacity of spirit.</p>
            <p>Joan passed away in 2021 at the age of 91. One evening she went to bed after watching a television programme about travelling, but never woke up again.</p>
